function sum(n1, n2){
    return parseFloat(n1) + parseFloat(n2);
}

function diff(n1, n2){
    return parseFloat(n1) - parseFloat(n2);
}

function mult(n1, n2){
    return parseFloat(n1) * parseFloat(n2);
}

function div(n1, n2){
    return parseFloat(n1) / parseFloat(n2);
}

export{sum, diff, mult, div}